/* Character classes */
#define LETTER 0
#define DIGIT 1
#define UNKNOWN 99

/* Token codes - from lexical analyzer */
#define INT_LIT 10
#define IDENT 11
#define FLOAT_LIT 12
#define ASSIGN_OP 20
#define ADD_OP 21
#define SUB_OP 22
#define MULT_OP 23
#define DIV_OP 24
#define LEFT_PAREN 25
#define RIGHT_PAREN 26

/* Reserved word codes */
#define FOR_CODE 30
#define IF_CODE 31
#define ELSE_CODE 32
#define WHILE_CODE 33
#define DO_CODE 34
#define INT_CODE 35
#define FLOAT_CODE 36

/* Additional token codes for parser */
#define LEFT_BRACE 40
#define RIGHT_BRACE 41
#define SEMICOLON 42
#define LT_OP 43      /* < */
#define GT_OP 44      /* > */
#define LE_OP 45      /* <= */
#define GE_OP 46      /* >= */
#define EQ_OP 47      /* == */
#define NE_OP 48      /* != */

/* Reserved words table */
struct {
    char *word;
    int code;
} reservedWords[] = {
    {"for", FOR_CODE},
    {"if", IF_CODE},
    {"else", ELSE_CODE},
    {"while", WHILE_CODE},
    {"do", DO_CODE},
    {"int", INT_CODE},
    {"float", FLOAT_CODE},
    {NULL, 0}
};

